alert("Número maior")
let n1 = parseInt(prompt("Insira o primeiro número:"))
let n2 = parseInt(prompt("Insira o segundo número:"))

if (n1 > n2) {
  alert("O maior número é o primeiro: "+n1)
} else {
  alert("O maior número é o segundo: "+n2)
}
